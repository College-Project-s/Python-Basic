teks = "Kelas Pemdas"

# Capitalize the first character of each word
print(teks.capitalize())

# Convert all characters to uppercase
print(teks.upper())

# Convert all characters to lowercase
print(teks.lower())

# Count the occurrences of 'a' in the string
print(teks.count('a'))

# Check if the string ends with 'a'
print(teks.endswith('a'))

# Find the index of the first occurrence of 'o' in the string
print(teks.find('o'))

# Check if all characters in the string are lowercase
print(teks.islower())

# Check if all characters in the string are uppercase
print(teks.isupper())

# Check if all characters in the string are digits
print(teks.isdigit())

# Remove leading and trailing whitespace from the string
print(teks.strip())

# Split the string into a list of words
print(teks.split())